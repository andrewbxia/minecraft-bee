            const p0 = totalpath[arridx(i - 1, totalpath.length)];
            const p1 = totalpath[i];
            const p2 = totalpath[arridx(i + 1, totalpath.length)];
            const p3 = totalpath[arridx(i + 2, totalpath.length)];


